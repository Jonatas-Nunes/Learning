medida1 = float(input('insira um valor da grandeza de medida em metros'))
milimetro = medida1 * 1000
centímetro = medida1 * 100
kilometros = medida1 / 1000
print('{} metros → {} milimetros'.format(medida1, milimetro))
print('{} metros → {} centímetros'.format(medida1, centímetro))
print('{} metros → {} kilômetros'.format(medida1, kilometros))